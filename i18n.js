function T(key, lang = 'ar') {
  const parts = key.split('.');
  let curr = window.I18N;
  for (const p of parts) {
    if (curr && curr[p] !== undefined) {
      curr = curr[p];
    } else {
      return key;
    }
  }
  if (typeof curr === 'object' && curr !== null) {
    return curr[lang] || curr['ar'] || key;
  }
  return curr || key;
}
